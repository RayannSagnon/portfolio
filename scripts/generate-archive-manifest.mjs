import fs from "fs";
import path from "path";
import matter from "gray-matter";
import readingTime from "reading-time";

const root = process.cwd();
const enDir = path.join(root, "content", "archive");
const frDir = path.join(root, "content", "archive", "fr");
const outFile = path.join(root, "content", "archiveManifest.ts");

const slugs = fs
  .readdirSync(enDir)
  .filter((f) => f.endsWith(".mdx"))
  .map((f) => f.replace(/\.mdx$/, ""))
  .sort();

function loadLocale(locale, dir) {
  const posts = {};
  for (const slug of slugs) {
    const filePath = path.join(dir, `${slug}.mdx`);
    if (!fs.existsSync(filePath)) {
      console.warn(`Missing ${locale} archive post: ${slug}`);
      continue;
    }
    const raw = fs.readFileSync(filePath, "utf-8");
    const { data, content } = matter(raw);
    posts[slug] = {
      frontmatter: data,
      content: content.trim(),
      readingTime: readingTime(content).text,
    };
  }
  return posts;
}

const manifest = {
  en: loadLocale("en", enDir),
  fr: loadLocale("fr", frDir),
};

const file = `// Generated by scripts/generate-archive-manifest.mjs — do not edit by hand.
import type { ArchiveFrontmatter } from "@/lib/archive/mdxUtils";

export type ArchiveManifestPost = {
  frontmatter: ArchiveFrontmatter;
  content: string;
  readingTime: string;
};

export type ArchiveManifest = Record<string, ArchiveManifestPost>;

export const archiveManifest: Record<"en" | "fr", ArchiveManifest> = ${JSON.stringify(manifest, null, 2)} as const;
`;

fs.writeFileSync(outFile, file, "utf-8");
console.log(`Wrote ${outFile} (${slugs.length} slugs)`);
